/**
 * cameraLimits.ts - Единый источник правды для границ камеры в графах
 * 
 * Используется для всех режимов графа (ForceGraph, EpochGraph, Timeline)
 * для обеспечения единообразного поведения скролла/панорамирования.
 */

export const CAMERA_LIMITS = {
  /**
   * Жесткие границы камеры (hard limits)
   * Камера не может выйти за эти пределы ни при каких условиях
   */
  minYearHard: -1000,
  maxYearHard: 2500,
  
  /**
   * Мягкий overscroll (в годах)
   * Позволяет пользователю проскроллить чуть дальше границ данных,
   * чтобы не было ощущения "упёрся в стену"
   * 
   * Можно также использовать процент от видимого диапазона (10%),
   * но фиксированное значение проще для понимания
   */
  overscrollYears: 150,
} as const;

